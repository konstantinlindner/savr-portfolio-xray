// Factory for generating minimal stock details for investor-held stocks
type StockSpec = {
	ticker: string
	name: string
	sector: string
	currentPrice: number
	dailyChange: number
	peRatio: number
	marketCap: string
	beta: number
	dividendYield: number
}

function generateStockDetail(spec: StockSpec): StockDetail {
	const volatility = Math.min(0.03, spec.beta * 0.015)
	return {
		ticker: spec.ticker,
		name: spec.name,
		description: `${spec.name} är ett bolag verksamt inom sektorn ${spec.sector.toLowerCase()}.`,
		currentPrice: spec.currentPrice,
		dailyChange: spec.dailyChange,
		currency: 'USD',
		metrics: {
			peRatio: spec.peRatio,
			marketCap: spec.marketCap,
			high52w: Math.round(spec.currentPrice * 1.25 * 100) / 100,
			low52w: Math.round(spec.currentPrice * 0.7 * 100) / 100,
			dividendYield: spec.dividendYield,
			beta: spec.beta,
			eps: Math.round((spec.currentPrice / spec.peRatio) * 100) / 100,
		},
		priceHistory: generatePriceHistory(spec.currentPrice, 90, volatility),
		transactions: [],
		news: [],
	}
}

